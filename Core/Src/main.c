static void crsf_recieve(uint8_t rx_data){

	static state_rec_t state = STATE_IDLE;
	static uint8_t index = 0;
	switch (state){
		case STATE_IDLE:
			if ((rx_data == CRSF_ADDRESS_FLIGHT_CONTROLLER) || (rx_data == CRSF_ADDRESS_CRSF_TRANSMITTER)) {
				HAL_GPIO_TogglePin(GPIOC, GPIO_PIN_13);
				//printf("--------adrr 0x%02x\n\r", rx_data);
				state = STATE_LENGHT;
				index = 0;
				rxbuff[index++] = rx_data;
			}
			break;
		case STATE_LENGHT:
			//printf("--------len 0x%02x\n\r", rx_data);
			if ((rx_data < 3 ) || (rx_data > CRSF_MAX_PAYLOAD_LEN + 2)) {
				state = STATE_IDLE;
			} else {
				state = STATE_DATA;
				rxbuff[index++] = rx_data;
			}
			break;
		case STATE_DATA:
			rxbuff[index++] = rx_data;
			if (index >=rxbuff[1] + 2) {
				state = STATE_IDLE;
				uint8_t crc_in = rxbuff[2 + rxbuff[1] - 1];
				uint8_t crc_calc = crc8_calc(&rxbuff[2], rxbuff[1] - 1);
				printf("addr =0x%02x, lenght = 0x%02x,type = 0x%02x, crc in =0x%02x, crc calc = 0x%02x\n\r",
						rxbuff[0],rxbuff[1], rxbuff[2],crc_in,crc_calc);
				if (crc_in == crc_calc) {
					processCRSFpacket(rxbuff);
					printf("CRC_OK\n\r");
				} else {
					printf("CRC WRONG\n\r");
				}
			}
			break;
	}
}

